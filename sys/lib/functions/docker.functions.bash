# dnames
# ------------------------------------------------------------------
dnames() { docker ps --format "{{.Names}}"; }
# ------------------------------------------------------------------
# din
# ------------------------------------------------------------------
din() { docker inspect "${1}"; }
# ------------------------------------------------------------------
# dip
# ------------------------------------------------------------------
dip()
{
    echoGold "IP Addresses of All Named Running Containers:"

    for DOC in `dnames`
    do
        IP="$(docker inspect -f '{{range .NetworkSettings.Networks}}{{.IPAddress}}{{end}}' "$DOC")"
        OUT+=$DOC'\t'$IP'\n'
    done

    echo -e "$OUT" | column -t
    unset OUT
}

